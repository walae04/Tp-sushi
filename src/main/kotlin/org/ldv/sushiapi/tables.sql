CREATE DATABASE bd_sushi_wouahi;
CREATE TABLE boxes ( box_id INT AUTO_INCREMENT PRIMARY KEY, nom VARCHAR (255) NOT NULL, nb_pieces INT, prix DECIMAL(5,2), nom_image VARCHAR(255));
CREATE TABLE saveurs ( saveur_id INT AUTO_INCREMENT PRIMARY KEY, nom VARCHAR(255) NOT NULL );
CREATE TABLE aliments ( aliment_id INT AUTO_INCREMENT PRIMARY KEY, nom VARCHAR(255) NOT NULL );
CREATE TABLE saveurs_boxes( fk_box_id INT DEFAULT NULL REFERENCES boxes (box_id) ON DELETE CASCADE ON UPDATE CASCADE, fk_saveur_id INT DEFAULT NULL REFERENCES saveurs (saveur_id) ON DELETE RESTRICT ON UPDATE CASCADE, PRIMARY KEY (fk_box_id,fk_saveur_id));
CREATE TABLE aliments_boxes( fk_box_id INT DEFAULT NULL REFERENCES boxes
    (box_id) ON DELETE CASCADE ON UPDATE CASCADE, fk_aliment_id INT DEFAULT NULL REFERENCES aliments (aliment_id) ON DELETE RESTRICT ON UPDATE CASCADE, PRIMARY KEY
                                 (fk_box_id, fk_aliment_id), quantite INT);
